package config

import (
	"fmt"
	"os"

	// MySQL 驱动，用于让 GORM 连接 MySQL 数据库。
	"gorm.io/driver/mysql"

	// GORM 是 Go 语言常用的 ORM 框架。
	// 本项目通过 GORM 操作 MySQL 数据库，简化增删改查代码。
	"gorm.io/gorm"
)

// DB 是全局数据库连接对象。
// 后端其他包，例如 controller、service 或 model，
// 可以通过 config.DB 来执行数据库查询和写入操作。
var DB *gorm.DB

func InitDB() {
	// ============================================================
	// 1. 从环境变量读取数据库连接字符串
	// ============================================================
	// MYSQL_DSN 中保存 MySQL 连接信息，例如：
	// 用户名:密码@tcp(127.0.0.1:3306)/数据库名?charset=utf8mb4&parseTime=True&loc=Local
	//
	// 使用环境变量而不是直接写在代码里，可以避免数据库账号和密码暴露在代码中，
	// 也方便在不同电脑或不同部署环境下修改数据库配置。
	dsn := os.Getenv("MYSQL_DSN")

	// 如果没有设置 MYSQL_DSN，后端无法连接数据库，直接终止程序。
	if dsn == "" {
		panic("未设置环境变量 MYSQL_DSN")
	}

	// ============================================================
	// 2. 使用 GORM 连接 MySQL
	// ============================================================
	// mysql.Open(dsn) 用于指定 MySQL 连接方式。
	// gorm.Open(...) 返回数据库连接对象。
	db, err := gorm.Open(mysql.Open(dsn), &gorm.Config{})
	if err != nil {
		panic("数据库连接失败")
	}

	// ============================================================
	// 3. 获取底层数据库连接
	// ============================================================
	// GORM 本身是 ORM 封装，如果需要执行一些底层 SQL 配置，
	// 例如设置时区，就需要先通过 db.DB() 获取底层 *sql.DB 对象。
	sqlDB, err := db.DB()
	if err != nil {
		panic("获取底层数据库连接失败")
	}

	// ============================================================
	// 4. 设置数据库会话时区
	// ============================================================
	// 本项目采集数据、历史趋势和报警记录都依赖时间字段。
	// 这里把数据库会话时区设置为 +08:00，避免时间写入和查询时出现偏移。
	// 对应论文中的历史数据查询、趋势展示和异常窗口时间范围记录。
	if _, err := sqlDB.Exec("SET time_zone = '+08:00'"); err != nil {
		panic("设置数据库时区失败")
	}

	// ============================================================
	// 5. 保存全局数据库连接对象
	// ============================================================
	// 将连接成功的 db 赋值给全局变量 DB。
	// 后续接口中可以直接使用 config.DB 访问数据库。
	DB = db

	fmt.Println("数据库连接成功")
}
//db.go 负责后端数据库初始化。它从环境变量 MYSQL_DSN 读取数据库连接字符串，通过 GORM 连接 MySQL，并设置数据库时区为 +08:00，
//保证采集时间、历史趋势时间和报警时间不会出现偏移。连接成功后，数据库对象会保存到 config.DB，供后端其他接口使用。