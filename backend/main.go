package main

import (
	// config 包中主要放数据库初始化等全局配置逻辑。
	// 这里调用 config.InitDB() 来建立 MySQL 数据库连接。
	"backend/config"

	// router 包中集中注册后端接口路由。
	// 前端页面、采集端上传数据、报警查询等接口都会在这里注册。
	"backend/router"

	// cors 用于解决前端跨域访问后端接口的问题。
	// 由于前端 Vue 开发服务器和 Go 后端端口通常不同，
	// 浏览器会产生跨域限制，因此需要开启 CORS。
	"github.com/gin-contrib/cors"

	// Gin 是 Go 语言中常用的轻量级 Web 框架。
	// 本项目使用 Gin 提供 REST 风格接口，供前端页面和采集端调用。
	"github.com/gin-gonic/gin"
)

func main() {
	// ============================================================
	// 1. 初始化数据库连接
	// ============================================================
	// InitDB 会连接 MySQL 数据库，并将数据库连接对象保存到全局变量中。
	// 后续 controller、service 或 model 中进行数据查询和写入时，
	// 都会使用这里初始化好的数据库连接。
	config.InitDB()

	// ============================================================
	// 2. 创建 Gin 服务实例
	// ============================================================
	// gin.Default() 会创建一个默认的 Gin 引擎，
	// 并自动包含日志中间件和异常恢复中间件。
	// 日志中间件可以在控制台打印每次请求信息；
	// 恢复中间件可以防止单个接口 panic 导致整个服务崩溃。
	r := gin.Default()

	// ============================================================
	// 3. 配置跨域访问
	// ============================================================
	// 前端 Vue 项目通常运行在 5173 或其他端口，
	// 后端 Gin 服务运行在 8080 端口。
	// 两者端口不同，浏览器会认为是跨域请求。
	// cors.Default() 允许前端正常请求后端接口，便于本地开发和系统联调。
	r.Use(cors.Default())

	// ============================================================
	// 4. 注册系统接口路由
	// ============================================================
	// RegisterRoutes 会统一注册本系统的后端接口，
	// 包括设备管理、历史数据查询、报警记录、统计分析、
	// 用户管理、报表展示等相关接口。
	// 这样 main.go 只负责启动流程，具体接口注册逻辑放到 router 包中。
	router.RegisterRoutes(r)

	// ============================================================
	// 5. 启动后端服务
	// ============================================================
	// 后端服务监听 8080 端口。
	// 前端页面和 Python 采集端会通过 http://localhost:8080/... 调用接口。
	// 例如，采集端清洗通过后的数据会上传到后端接口，
	// 后端再写入 energy_history 表并触发后续业务逻辑。
	r.Run(":8080")
}
//main.go 是后端服务的启动入口。程序启动时先初始化 MySQL 数据库连接，然后创建 Gin 服务，开启跨域支持，注册所有业务接口，最后监听 8080 端口。
// 前端页面和 Python 采集端都通过这个后端服务进行数据交互。